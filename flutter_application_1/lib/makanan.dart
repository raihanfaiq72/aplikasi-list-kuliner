class Makanan {
  final String nama;
  final String deskripsi;
  final String harga;
  final String gambar;
  final List<String> gambarDetail;
  final int kalori;
  final String waktuBuka;
  final List<String> bahan;

  Makanan({
    required this.nama,
    required this.deskripsi,
    required this.harga,
    required this.gambar,
    required this.gambarDetail,
    required this.kalori,
    required this.waktuBuka,
    required this.bahan,
  });
}
